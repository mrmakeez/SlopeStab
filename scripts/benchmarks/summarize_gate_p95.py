from __future__ import annotations

import argparse
import json
import math
from pathlib import Path
from typing import Any


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


ROOT = _repo_root()


def _load_json(path: Path) -> dict[str, Any]:
    parsed = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(parsed, dict):
        raise ValueError(f"Expected object JSON at: {path}")
    return parsed


def _collect_manifest_files(root: Path) -> list[Path]:
    manifests = [p for p in root.glob("*/manifest.json") if p.is_file()]
    manifests.sort(key=lambda p: p.parent.name)
    return manifests


def _percentile(values: list[float], pct: float) -> float:
    if not values:
        return float("nan")
    if len(values) == 1:
        return values[0]
    ordered = sorted(values)
    rank = (pct / 100.0) * (len(ordered) - 1)
    low = int(math.floor(rank))
    high = int(math.ceil(rank))
    if low == high:
        return ordered[low]
    weight = rank - low
    return ordered[low] + weight * (ordered[high] - ordered[low])


def _summarize_stage_seconds(manifests: list[dict[str, Any]], stage_name: str) -> dict[str, Any]:
    values: list[float] = []
    for manifest in manifests:
        stages = manifest.get("stages")
        if not isinstance(stages, dict):
            continue
        stage = stages.get(stage_name)
        if not isinstance(stage, dict):
            continue
        value = stage.get("seconds")
        if isinstance(value, (int, float)):
            values.append(float(value))
            continue
        value_total = stage.get("seconds_total")
        if isinstance(value_total, (int, float)):
            values.append(float(value_total))
    return {
        "count": len(values),
        "p50_seconds": _percentile(values, 50.0),
        "p95_seconds": _percentile(values, 95.0),
        "max_seconds": max(values) if values else float("nan"),
        "values_seconds": values,
    }


def _summarize_total_seconds(manifests: list[dict[str, Any]]) -> dict[str, Any]:
    totals: list[float] = []
    for manifest in manifests:
        stages = manifest.get("stages")
        if not isinstance(stages, dict):
            continue
        total = 0.0
        count = 0
        for stage_name in ("verify", "test"):
            stage = stages.get(stage_name)
            if not isinstance(stage, dict):
                continue
            value = stage.get("seconds")
            if isinstance(value, (int, float)):
                total += float(value)
                count += 1
                continue
            value_total = stage.get("seconds_total")
            if isinstance(value_total, (int, float)):
                total += float(value_total)
                count += 1
        if count:
            totals.append(total)
    return {
        "count": len(totals),
        "p50_seconds": _percentile(totals, 50.0),
        "p95_seconds": _percentile(totals, 95.0),
        "max_seconds": max(totals) if totals else float("nan"),
        "values_seconds": totals,
    }


def _clean_nan(payload: Any) -> Any:
    if isinstance(payload, float) and math.isnan(payload):
        return None
    if isinstance(payload, list):
        return [_clean_nan(item) for item in payload]
    if isinstance(payload, dict):
        return {key: _clean_nan(value) for key, value in payload.items()}
    return payload


def main() -> int:
    parser = argparse.ArgumentParser(
        description=(
            "Summarize rolling p95 wall time from gate baseline manifests "
            "(generated by capture_gate_baseline.py or run_guarded_gate.py)."
        )
    )
    parser.add_argument(
        "--input-root",
        default="tmp/gate_baselines",
        help="Manifest root directory to scan.",
    )
    parser.add_argument(
        "--window",
        type=int,
        default=20,
        help="Number of most-recent manifests to include.",
    )
    parser.add_argument(
        "--output",
        default=None,
        help="Optional JSON output file path.",
    )
    args = parser.parse_args()

    if args.window <= 0:
        raise ValueError("--window must be greater than zero.")

    input_root = (ROOT / args.input_root).resolve()
    manifest_files = _collect_manifest_files(input_root)
    selected_files = manifest_files[-args.window :]
    manifests = [_load_json(path) for path in selected_files]

    summary = {
        "schema_version": "gate-p95-summary-v1",
        "input_root": str(input_root),
        "window": args.window,
        "selected_manifest_count": len(selected_files),
        "selected_manifests": [str(path.relative_to(ROOT).as_posix()) for path in selected_files],
        "verify": _summarize_stage_seconds(manifests, "verify"),
        "test": _summarize_stage_seconds(manifests, "test"),
        "total_verify_plus_test": _summarize_total_seconds(manifests),
    }
    summary = _clean_nan(summary)

    output_text = json.dumps(summary, indent=2) + "\n"
    if args.output is not None:
        output_path = (ROOT / args.output).resolve()
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(output_text, encoding="utf-8")

    print(output_text, end="")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
